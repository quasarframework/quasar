<template>
  <div class="q-layout-padding">
    <div style="max-width: 600px" class="q-gutter-y-md">
      <h1>Input Validate</h1>

      <div class="q-gutter-sm">
        <q-radio v-model="type" val="filled" label="Filled" />
        <q-radio v-model="type" val="outlined" label="Outlined" />
        <q-radio v-model="type" val="standout" label="Standout" />
        <q-radio v-model="type" val="standard" label="Standard" />
        <q-radio v-model="type" val="borderless" label="Borderless" />
      </div>

      <div class="text-h6">
        Internal validation
        <q-btn label="Reset" @click="reset" color="primary" flat />
      </div>

      <q-input
        ref="input1"
        v-bind="{[type]: true}"
        v-model="model1"
        label="Label *"
        :rules="[
          val => !!val || '* Required'
        ]"
      />

      <q-input
        ref="input2"
        v-bind="{[type]: true}"
        v-model="model2"
        label="len <= 3"
        counter
        hint="Type something"
        :rules="[
          val => val.length <= 3 || 'Please use maximum 3 characters'
        ]"
      >
        <q-icon slot="append" name="close" @click="model2 = ''" class="cursor-pointer" />
      </q-input>

      <q-input
        ref="input3"
        v-bind="{[type]: true}"
        v-model="model3"
        label="Required, Lazy, Len < 2"
        counter
        hint="Validation starts after first blur"
        :rules="[
          val => !!val || '* Required',
          val => val.length < 2 || 'Please use maximum 1 character',
        ]"
        lazy-rules
      />

      <div class="text-h6">
        Build in validation
        <q-btn label="Reset" @click="reset" color="primary" flat />
      </div>

      <q-input
        ref="input4"
        v-bind="{[type]: true}"
        v-model="model4"
        label="Date (eg: 01/01/20) *"
        :rules="[ 'date' ]"
      />

      <q-input
        ref="input5"
        v-bind="{[type]: true}"
        v-model="model5"
        label="Hex color [eg: #ABCDEF] *"
        :rules="[ 'hexColor' ]"
      />

      <q-input
        ref="input6"
        v-bind="{[type]: true}"
        v-model="model6"
        label="Rgb color [eg: rgb(64,64,64)] *"
        :rules="[ 'rgbColor' ]"
      />

      <div class="text-h6">
        Complex validation
        <q-btn label="Reset" @click="reset" color="primary" flat />
      </div>

      <q-input
        ref="input1"
        v-bind="{[type]: true}"
        v-model="model7"
        label="Input A *"
        :rules="[
          { message (field) { return `${field} is required` }, handler (val) { return !!val } }
        ]"
      />

      <q-input
        ref="input1"
        v-bind="{[type]: true}"
        v-model="model8"
        label="Compare that Input (B) with Input A"
        :rules="[
          { args: model7, label: 'Input B', message (field) { return `${field} must be equal to the other field` }, handler (val, other) { return val === other } }
        ]"
      />

      <q-input
        ref="input1"
        v-bind="{[type]: true}"
        v-model="model9"
        label="Input C is required and must be unique (not in ['1', '2', '3'])"
        validation-label="Input C"
        :rules="[
          { lazy: false, message (field) { return `${field} is required` }, handler (val) { return !!val } },
          { lazy: true, message (field) { return `${field} must be unique` }, handler: isUnique }
        ]"
      />

      <div class="text-h6 q-mt-xl">External validation</div>
      <div class="q-gutter-sm">
        <q-toggle v-model="error" label="Error state" />
        <q-radio v-model="errorMessage" val="First error" label="First error" />
        <q-radio v-model="errorMessage" val="Second error" label="Second error" />
      </div>
      <q-input
        ref="inputExternal"
        v-bind="{[type]: true}"
        v-model="modelExternal"
        label="Label"
        hint="Hint"
        :error="error"
        :error-message="errorMessage"
      />

      <q-input
        ref="inputExternal"
        v-bind="{[type]: true}"
        v-model="modelExternal"
        label="Label"
        hint="Hint"
        :error="error"
        style="margin-bottom: 30px"
      >
        <div slot="error">Slotted error message</div>
        <div slot="error">Second slotted error message</div>
      </q-input>
    </div>
  </div>
</template>

<script>
export default {
  data () {
    const n = 9

    const data = {
      n,
      type: 'filled',
      modelExternal: '',
      error: false,
      errorMessage: 'First error'
    }

    for (let i = 1; i <= n; i++) {
      data['model' + i] = ''
    }

    return data
  },

  methods: {
    reset () {
      for (let i = 1; i <= this.n; i++) {
        this.$refs['input' + i].resetValidation()
      }
    },
    async isUnique (value) {
      await new Promise(resolve => window.setTimeout(resolve, 2000))
      return ['1', '2', '3'].indexOf(value) === -1
    }
  }
}
</script>
